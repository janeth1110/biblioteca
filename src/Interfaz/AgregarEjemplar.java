    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        if (jTFEjemplar.getText().trim().isEmpty() || jTFUbicacion.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Verifique que no hayan campos vacios", "Todos los campos son obligatorios", JOptionPane.WARNING_MESSAGE);
        } else {
            ejemplar.setIdLibro(idLibro);
            ejemplar.setNumEjemplar(Integer.parseInt(jTFEjemplar.getText()));
            ejemplar.setUbicacion(jTFUbicacion.getText());
            ejemplar.setIdEstado(1);
            if (adej.agregarEjemplar(ejemplar)) {
                JOptionPane.showMessageDialog(null, "Ejemplar agregado exitosamente!", "En horabuena!", JOptionPane.INFORMATION_MESSAGE);
                limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Ha ocurrido un problema, vuelva a intentarlo mas tarde", "Ha ocurrido un error", JOptionPane.ERROR_MESSAGE);
            }
            jTEjemplares.setModel(mostrarEjemplares(idLibro));
            tabla.resizeColumnWidth(jTEjemplares);
        }
    }//GEN-LAST:event_jBGuardarActionPerformed

