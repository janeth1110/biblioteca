    public DefaultTableModel mostrarEjemplares(int d) {
        List<Ejemplar> ejemplares2 = new ArrayList<Ejemplar>();
        DefaultTableModel res = new DefaultTableModel();
        res.addColumn("Ejemplar");
        res.addColumn("Ubicacion");
        res.addColumn("Disponibilidad");

        aej.mostrarEjemplar(d);
        ejemplares2 = aej.mostrarEjemplar(d);
        int i = 0;
        for (Ejemplar l : ejemplares2) {
            res.addRow(new Object[]{});
            res.setValueAt(l.getNumEjemplar(), i, 0);
            res.setValueAt(l.getUbicacion(), i, 1);
            if (l.getIdEstado() == 2) {
                res.setValueAt("Prestado", i, 2);
            } else {
                res.setValueAt("Disponible", i, 2);
            }
            i++;
        }
        return res;
    }

    public Ejemplar obtenerEjemplar(int x) {
        ejemplar = aej.obtenerEjemplar(x);
        libro = ali.obtenerLibro(x);
        jLNombreLibro1.setText(libro.getTitulo());
        idLibro = x;
        return ejemplar;
    }

