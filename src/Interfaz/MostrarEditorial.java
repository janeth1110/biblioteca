    public DefaultTableModel mostrarEditorial() {
        List<Editorial> editoriales2 = new ArrayList<Editorial>();
        DefaultTableModel res = new DefaultTableModel();
        res.addColumn("Código");
        res.addColumn("Nombre");
        res.addColumn("Direccion");
        res.addColumn("Telefono");

        ae.mostrarEditorial();
        editoriales2 = ae.mostrarEditorial();
        int i = 0;
        for (Editorial e : editoriales2) {
            res.addRow(new Object[]{});
            res.setValueAt(e.getId(), i, 0);
            res.setValueAt(e.getNombre(), i, 1);
            res.setValueAt(e.getDireccion(), i, 2);
            res.setValueAt(e.getTelefono(), i, 3);
            i++;
        }

        return res;
    }

    public String selected() {
        String id = null;
        TableModel tablaModelo;
        tablaModelo = (TableModel) jTEditoriales.getModel();
        id = String.valueOf(tablaModelo.getValueAt(jTEditoriales.getSelectedRow(), 0));

        if (id.isEmpty()) {
//            JOptionPane.showMessageDialog(null, id);
            return "Debe seleccionar un dato";
        } else {
//            JOptionPane.showMessageDialog(null, "Debe seleccionar una Editorial");
            return id;

        }

    }

    public void tamaños(JTable tabla) {
        TableColumnModel columnModel = tabla.getColumnModel();
        columnModel.getColumn(0).setPreferredWidth(1);
        columnModel.getColumn(1).setPreferredWidth(200);
        columnModel.getColumn(2).setPreferredWidth(250);
        columnModel.getColumn(3).setPreferredWidth(30);
    }

