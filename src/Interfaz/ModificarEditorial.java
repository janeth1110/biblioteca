    private void jBModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBModificarActionPerformed
        // TODO add your handling code here:
        if (jtfNombre.getText().trim().isEmpty()
                || jtfDireccion.getText().trim().isEmpty()
                || jtfTelefono.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Verifique que no hayan campos vacios");
        } else {
            modificar(temp, jtfNombre.getText(), jtfDireccion.getText(), jtfTelefono.getText());
            MostrarEditorial me = new MostrarEditorial();
            me.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jBModificarActionPerformed

