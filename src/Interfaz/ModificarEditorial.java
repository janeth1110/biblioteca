    public void mostrar(int x) {
        Editorial edi = new Editorial();
        edi = ae.obtenerEditorial(x);
        jtfNombre.setText(edi.getNombre().toString());
        jtfDireccion.setText(edi.getDireccion().toString());
        jtfTelefono.setText(edi.getTelefono().toString());
    }

    public void modificar(int id, String nombre, String direccion, String telefono) {
        ae.modificarEditorial(id, nombre, direccion, telefono);
    }

