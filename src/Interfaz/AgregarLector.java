    private void jBAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarActionPerformed
        // TODO add your handling code here:
        if (jtfNombres.getText().trim().isEmpty()
                || jTFApellidos.getText().trim().isEmpty()
                || jTFTelefono.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Verifique que no hayan campos vacios");
        } else {
            AdministrarLector al = new AdministrarLector();
            Lector le = new Lector();
            le.setNombre(jtfNombres.getText());
            le.setApellido(jTFApellidos.getText());
            le.setSexo(SexoSeleccionado(jCBSexo));
            le.setFecha_nac(fecha_nacimiento(jDCFechaNacimiento));
            le.setCondicion(CondicionSeleccionada(jCBCondicion));
            le.setTelefono(jTFTelefono.getText());

            if (al.agregarLector(le)) {
//            JOptionPane.showMessageDialog(null, "Lector agregado exitosamente");
                limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Ha sucedido un error, por favor revise los datos ingresados");
            }
        }
    }//GEN-LAST:event_jBAgregarActionPerformed

    private void jTFTelefonoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTFTelefonoKeyTyped
        // TODO add your handling code here:
        char caracter = evt.getKeyChar();
        if ((!Character.isDigit(caracter))
                || jTFTelefono.getText().length() >= 9) {
            evt.consume();
        }
    }//GEN-LAST:event_jTFTelefonoKeyTyped

