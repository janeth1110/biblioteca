            //SimpleDateFormat sdf = new SimpleDateFormat(formato);
            //String fecha = (String.valueOf(sdf.format(date)));
            sqlDate = new java.sql.Date(date.getTime());
            //JOptionPane.showMessageDialog(null, sqlDate);
